color = input("Enter your color in ALL CAPS")

if (color == "RED"):
    print("(255, 0, 0)")
elif (color == "ORANGE"):
    print("(255, 128, 0)")
elif (color == "YELLOW"):
    print("(255, 255, 0)")
elif (color == "GREEN"):
    print("(0, 255, 0)")
elif (color == "BLUE"):
    print("(0, 0, 255)")
elif (color == "INDIGO"):
    print("(128, 0, 255)")
elif (color == "VIOLET"):
    print("(255, 0 255)")
else:
    print("Unknown Color, please try again")